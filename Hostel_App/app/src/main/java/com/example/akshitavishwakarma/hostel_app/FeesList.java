package com.example.akshitavishwakarma.hostel_app;

public class FeesList {

    private int stu_Id;
    private String submit_Date;
    private String stu_Name;
    private String fees_Amount;
    private String received_Fees;
    private String due_Fees;
    private String receipt_Number;
    private String received_By;


    public int getstu_Id() {
        return stu_Id;
    }

    public void setstu_Id(int stu_Id) {
        this.stu_Id = stu_Id;
    }

    public String getsubmit_Date() {
        return submit_Date;
    }

    public void setsubmit_Date(String submit_Date) {
        this.submit_Date = submit_Date;
    }

    public String getstu_Name() {
        return stu_Name;
    }

    public void setstu_Name(String stu_Name) {
        this.stu_Name = stu_Name;
    }

    public String getfees_Amount() {
        return fees_Amount;
    }

    public void setfees_Amount(String fees_Amount) {
        this.fees_Amount = fees_Amount;
    }

    public String getreceived_Fees() {
        return received_Fees;
    }

    public void setreceived_Fees(String received_Fees) {
        this.received_Fees = received_Fees;
    }

    public String getdue_Fees() {
        return due_Fees;
    }

    public void setdue_Fees(String due_Fees) {
        this.due_Fees = due_Fees;
    }

    public String getreceipt_Number() {
        return receipt_Number;
    }

    public void setreceipt_Number(String receipt_Number) {
        this.receipt_Number = receipt_Number;
    }

    public String getreceived_By() {
        return received_By;
    }

    public void setreceived_By(String received_By) {
        this.received_By = received_By;
    }
}
